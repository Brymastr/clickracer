	 * Store a newly created resource in storage.
	 *
	 * @return Response
	 */
	public function store()
	{
        if(Auth::check()) {
            $user = Auth::user();
            $date = new \DateTime;

            $score = new Score;
            $score->score = Input::get('score');
            $score->user_id = $user->id;
            $score->created_at = $date;
            $score->updated_at = $date;
            $score->save();
            return 1;
        }
        return 0;

	}


